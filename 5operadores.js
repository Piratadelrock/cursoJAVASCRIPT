'use strict';

var numero = 1;
var numero2 = 5;

//operadores basicos

// suma
var resultadosuma = numero + numero2;
// resta
var resultadoresta = numero - numero2;
// division
var resultadodivision = numero / numero2;
// multiplicacion
var resultadomultiplicacion = numero*numero2;

console.log("resultado suma: " + resultadosuma);
console.log("resultado resta: " + resultadoresta);
console.log("resultado division: " + resultadodivision);
console.log("resultado multiplicacion: " + resultadomultiplicacion);

// operadores de incremento
resultadosuma ++;
console.log("Incrementamos ++ " + resultadosuma);
resultadosuma --;
console.log("restamos -- " + resultadosuma);